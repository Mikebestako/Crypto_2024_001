function countdown(element, countDownDate, button) {

    // Fetch the display element
    var el = document.getElementById(element);

    // Set the timer
    var interval = setInterval(function() {
        var now = new Date().getTime();
        var distance = countDownDate - now;
        var inMinutes = Math.floor((distance % (1000 * 60 * 60)) / (1000 * 60));
        var days = Math.floor(distance / (1000 * 60 * 60 * 24))
        var hours = Math.floor((distance %(1000 * 60 * 60 * 24)) / (1000 * 60 * 60));
        var minutes = Math.floor((distance % (1000 * 60 * 60)) / (1000 * 60));
        var seconds = Math.floor((distance % (1000 * 60)) / 1000);


        if(minutes > 0) {
            var minute_text = minutes + (minutes > 1 ? ' minutes' : ' minute');
        } else {
            var minute_text = '';
        }

        if(hours > 0) {
            var hour_text = hours + (hours > 1 ? ' hours' : ' hour');
        } else {
            var hour_text = ''
        }

        if(days > 0){
            var day_text = days + (days > 1 ? ' days': ' day');
        } else {
            var day_text = '';
        }

        var second_text = seconds > 1 ? '' : '';
        //el.value = minute_text + ' ' + seconds + ' ' + second_text + '';
        //el.value = hour_text + ' ' + minute_text + ' ' + seconds + ' ' + second_text + 'seconds';
        el.value = day_text + ' ' + hour_text + ' ' + minute_text + ' ' + seconds + ' ' + second_text + 'seconds';

        if(minute_text == '' && hour_text == '' && day_text == ''){
            if(seconds < 0){
                seconds = 0;
            }
        }

        if(seconds == 0) {
            if(minute_text == '' && hour_text == '') {
                (el.value = "STOP!");  
                el.style.backgroundColor = 'pink';
                clearInterval(interval);
                button.disabled = false;     
                return
            }
        } else {
            el.style.backgroundColor = "blanchedalmond";
        }

    }, 1000);
}


